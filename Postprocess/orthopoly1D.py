import numpy as np
import jacobiPol as jp
#****************************************************************************************



#****************************************************************************************
# Definition                                                                            *
#****************************************************************************************
def orthopoly1D(x = None,n = None): 
    x=np.matrix(x)
    p = np.zeros((n + 1,x.shape[0]))
    for i in np.arange(0,n+1).reshape(-1):
        p[i,:] = jp.jacobiP(x,0,0,i) * np.sqrt((2 * i + 1) / 2)
    return p
